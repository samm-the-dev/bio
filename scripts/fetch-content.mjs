/**
 * Build-time content fetch: Sanity + local markdown -> static TypeScript modules.
 * Run from project root: node scripts/fetch-content.mjs
 *
 * Generates src/data/settings.ts, src/data/projects.ts, and src/data/posts.ts (gitignored).
 * Settings and projects come from Sanity (public CDN, no auth needed).
 * Blog posts come from local markdown files in content/posts/.
 */
import { createClient } from '@sanity/client';
import { writeFileSync, mkdirSync, readdirSync, readFileSync } from 'fs';
import matter from 'gray-matter';
import { marked } from 'marked';
import { createHighlighter } from 'shiki';

function escapeXml(str) {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

const client = createClient({
  projectId: 'wqqh5015',
  dataset: 'production',
  useCdn: true,
  apiVersion: '2025-03-01',
});

const SETTINGS_QUERY = `*[_type == "siteSettings"][0]{
  name, descriptor, githubUrl, repoUrl,
  tagline, taglineAttribution, taglineUrl,
  intro, projectsTeaser, aboutTeaser,
  aboutImprov, aboutMovies, aboutTtrpgs, aboutCode,
  "socialLinks": coalesce(socialLinks[]{ label, href }, [])
}`;

const PROJECTS_QUERY = `*[_type == "project"] | order(displayOrder asc){
  name, "slug": slug.current, category,
  description, tech, link, repo, displayOrder, status
}`;

const HEADER = '// Auto-generated by scripts/fetch-content.mjs — do not edit\n';

const [settings, projects] = await Promise.all([
  client.fetch(SETTINGS_QUERY),
  client.fetch(PROJECTS_QUERY),
]);

// Set up syntax highlighting with VS Code's Dark+ theme
const highlighter = await createHighlighter({
  themes: ['dark-plus'],
  langs: ['typescript', 'javascript', 'html', 'css', 'json', 'bash', 'markdown'],
});

const renderer = {
  code({ text, lang }) {
    const language = highlighter.getLoadedLanguages().includes(lang) ? lang : 'text';
    return highlighter.codeToHtml(text, { lang: language, theme: 'dark-plus' });
  },
};

marked.use({ renderer });

// Read blog posts from local markdown files
const postsDir = 'content/posts';
const postFiles = readdirSync(postsDir).filter((f) => f.endsWith('.md'));

const posts = postFiles
  .map((file) => {
    const raw = readFileSync(`${postsDir}/${file}`, 'utf-8');
    const { data, content } = matter(raw);
    if (!data.publishedAt) return null;

    // Resolve relatedProjects slugs to { name, slug } objects
    const relatedProjects = (data.relatedProjects || [])
      .map((slug) => projects.find((p) => p.slug === slug))
      .filter(Boolean)
      .map(({ name, slug }) => ({ name, slug }));

    return {
      title: data.title,
      slug: data.slug,
      excerpt: data.excerpt,
      body: marked(content),
      publishedAt: new Date(data.publishedAt).toISOString(),
      tags: data.tags || null,
      relatedProjects: relatedProjects.length > 0 ? relatedProjects : null,
    };
  })
  .filter(Boolean)
  .sort((a, b) => b.publishedAt.localeCompare(a.publishedAt));

if (!settings) {
  console.error('No siteSettings document found in Sanity.');
  process.exit(1);
}

mkdirSync('src/data', { recursive: true });

writeFileSync(
  'src/data/settings.ts',
  `${HEADER}import type { SiteSettings } from '@/lib/queries';\n\nexport const settings: SiteSettings = ${JSON.stringify(settings, null, 2)};\n`,
);

writeFileSync(
  'src/data/projects.ts',
  `${HEADER}import type { Project } from '@/lib/queries';\n\nexport const projects: Project[] = ${JSON.stringify(projects, null, 2)};\n`,
);

writeFileSync(
  'src/data/posts.ts',
  `${HEADER}import type { BlogPost } from '@/lib/queries';\n\nexport const posts: BlogPost[] = ${JSON.stringify(posts, null, 2)};\n`,
);

// Generate RSS feed
const siteUrl = 'https://samm.bio';
const feedItems = posts
  .map(
    (post) => `    <item>
      <title>${escapeXml(post.title)}</title>
      <link>${siteUrl}/blog/${post.slug}</link>
      <guid isPermaLink="true">${siteUrl}/blog/${post.slug}</guid>
      <description>${escapeXml(post.excerpt)}</description>
      <pubDate>${new Date(post.publishedAt).toUTCString()}</pubDate>
    </item>`,
  )
  .join('\n');

const feed = `<?xml version="1.0" encoding="UTF-8"?>
<rss version="2.0" xmlns:atom="http://www.w3.org/2005/Atom">
  <channel>
    <title>ADHDev</title>
    <link>${siteUrl}/blog</link>
    <description>My scatterbrained dev journey with Claude Code</description>
    <language>en-us</language>
    <atom:link href="${siteUrl}/feed.xml" rel="self" type="application/rss+xml"/>
${feedItems}
  </channel>
</rss>
`;

writeFileSync('public/feed.xml', feed);

console.log('Content fetched and written to src/data/ + public/feed.xml.');
